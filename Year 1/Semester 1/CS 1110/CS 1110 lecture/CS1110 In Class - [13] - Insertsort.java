public class Insertsort
{

    public static void main(String[] args)
	{
        char [] a = new char[1];
    }
    
    public static void insertSort(char [] a)
    {
        int unsorted;
        int inner;
        char temp;
        unsorted = 1;
        while(unsorted < a.length)
        {
            temp = a[unsorted];
            inner = unsorted;
        }
    }
}
